package com.example.hotelreservation.model;

/**
 * Enum ini merepresentasikan status dari sebuah reservasi.
 * RESERVED  : Menandakan bahwa reservasi aktif atau sedang berlangsung.
 * CANCELED  : Menandakan bahwa reservasi telah dibatalkan.
 */
public enum ReservationStatus {
    RESERVED,
    CANCELED
}
